<template>
  <div class="tab-control">
    <div
      class="tab-control-item"
      v-for="(item,key) in title"
      :key="item"
      @click="itemClick(key)"
      :class="{active:key === currentIndex}"
    >
      <span>{{item}}</span>
    </div>
  </div>
</template>

<script>
export default {
  data() {
    return {
      currentIndex: 0
    };
  },
  props: {
    title: {
      type: Array,
      default() {
        return [];
      }
    }
  },
  methods: {
    itemClick(index) {
      this.currentIndex = index;
      this.$emit("tabClick", index);
    }
  }
};
</script>

<style>
